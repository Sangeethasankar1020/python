import math

# Constants
print("pi:", math.pi)
print("e:", math.e)

# Basic functions
print("Square root of 16:", math.sqrt(16))
print("Factorial of 5:", math.factorial(5))
print("Greatest common divisor of 28 and 35:", math.gcd(28, 35))
print("2 raised to the power of 3:", math.pow(2, 3))
print("Natural logarithm of 10:", math.log(10))

# Trigonometric functions
angle = math.pi / 4  # 45 degrees in radians
print("sin(45 degrees):", math.sin(angle))
print("cos(45 degrees):", math.cos(angle))
print("tan(45 degrees):", math.tan(angle))

# Hyperbolic functions
print("sinh(1):", math.sinh(1))
print("cosh(1):", math.cosh(1))

# Angular conversion
print("180 degrees to radians:", math.radians(180))
print("Ï€ radians to degrees:", math.degrees(math.pi))

# Special functions
print("Ceiling of 4.2:", math.ceil(4.2))
print("Floor of 4.8:", math.floor(4.8))
print("Absolute value of -5:", math.fabs(-5))
